-- Strokes Gained Total
CREATE TABLE SG_TOTAL (
    PLAYER_ID SERIAL,
    PLAYER VARCHAR(255),
    "TOTAL SG:T" FLOAT,
    "TOTAL SG:T2G" FLOAT,
    "TOTAL SG:P" FLOAT,
    "MEASURED ROUNDS" INTEGER,
    TOURNAMENT_DATE DATE,
    TOURNAMENT_NAME VARCHAR(255),
    PRIMARY KEY (PLAYER_ID)
);

-- Strokes Gained T2G
CREATE TABLE SG_T2G (
    PLAYER_ID SERIAL,
    PLAYER VARCHAR(255),
    "SG:OTT" FLOAT,
    "SG:APR" FLOAT,
    "SG:ARG" FLOAT,
    "MEASURED ROUNDS" INTEGER,
    TOURNAMENT_DATE DATE,
    TOURNAMENT_NAME VARCHAR(255),
    PRIMARY KEY (PLAYER_ID)
);

-- Birdie Bogey Ratio (Pars, Eagles, Double Bogeys)
CREATE TABLE BIRDIE_BOGEY_RATIO (
    PLAYER_ID SERIAL,
    PLAYER VARCHAR(255),
    "TOTAL BIRDIES AND BETTER" INTEGER,
    "TOTAL BOGEYS AND WORSE" INTEGER,
    TOURNAMENT_DATE DATE,
    TOURNAMENT_NAME VARCHAR(255),
    PRIMARY KEY (PLAYER_ID)
);

